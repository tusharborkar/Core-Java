package Concatination;

public class Concatination {

	public static void main(String[] args) {
		
		
		int i=1000;
		String s="tushar"+i;
		System.out.println(s);
		
		double d=1234;
		String s2 ="borkar" + d;
		System.out.println(s2);
		
		boolean b=true;
		System.out.println(s2+b);
		
	}
}
